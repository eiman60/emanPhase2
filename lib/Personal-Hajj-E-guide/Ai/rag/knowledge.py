def get_glossary_for_query(query: str) -> str:
    q_norm = collapse_ws(normalize_arabic(query)).lower()
    injected: list[str] = []
    seen: set[str] = set()
    for term, triggers in _GLOSSARY_TRIGGERS.items():
        if term in seen:
            continue
        for trigger in triggers:
            if normalize_arabic(trigger) in q_norm:
                injected.append(GLOSSARY[term])
                seen.add(term)
                break
    if not injected:
        return ""
    return "📖 مصطلحات:\n" + "\n".join(f"• {d}" for d in injected)


